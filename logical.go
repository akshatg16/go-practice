package main

import "fmt"

func main() {

    fmt.Println(logical)

    fmt.Println(true && false)
    fmt.Println(false && true)
    fmt.Println(true && true)
    fmt.Println(false && false)
    fmt.Println(true || false)
    fmt.Println(false || true)
    fmt.Println(true || true)
    fmt.Println(false || false)
    fmt.Println(true || false)
    fmt.Println(!true)
}
